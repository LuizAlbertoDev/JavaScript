

const altura = 1.92
const peso = 110

const imc = peso / Math.pow(altura,2)


if (imc >40) {
    console.log ("Você esta com Obesidade Grave com IMC de",imc)
}

else if (imc >30) {
    console.log ("Você esta com Obeso com IMC de",imc)
}

else if (imc >25) {
    console.log ("Você esta com Acima do peso com IMC de",imc)
}

else if (imc >18.5) {
    console.log ("Você esta com Peso Normal com IMC de",imc)
}

else {
    console.log ("Você esta com Abaixo do peso com IMC de",imc)
}